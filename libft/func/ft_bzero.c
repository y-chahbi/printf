#include "../libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	dex;

	dex = 0;
	while (dex < n && n != '0')
	{
		((char *)s)[dex] = '0';
		dex++;
	}
	((char *)s)[dex] = '\0';
}
