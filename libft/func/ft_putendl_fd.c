#include "../libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	dex;

	dex = 0;
	while (s[dex] != '\0')
	{
		write(fd, &s[dex], 1);
		dex++;
	}
	write(fd, "\n", 1);
}
