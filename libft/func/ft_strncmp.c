int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int				d;
	unsigned char	t;

	d = 0;
	while (d < n)
	{
		t = (s1[d] - s2[d]);
		d++;
	}
	return ((char)t);
}
