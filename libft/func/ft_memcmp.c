#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int	d;
	int	t;

	d = 0;
	t = 0;
	while (d < n)
	{
		t = t + (((char *)s1)[d] - ((char *)s2)[d]);
		d++;
	}
	return (t);
}
